#include <iostream>

int main()
{
    // TYPES
    // 34u unsigned int
    // 23l long int
    // 32ULL unsigned long long int
    // 32.03f float
    // 55.953l long double
    int i = 31;
    std::cout << "Subtract ULL: " << 30ULL - i << '\n';
    std::cout << "Subtract F: " << 1E-3F - 0.045F << '\n';
    std::cout << "\145" << '\n';
    std::cout << "\77\50\n" << '\n';
    std::cout << "\77\t\50\n" << '\n';
    return 0;
}